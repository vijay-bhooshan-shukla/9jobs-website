export const siteRouteMap = Object.fromEntries(siteRoutes.map((route) => [route.path, route]));

export function absoluteUrl(path = "/") {
  if (path.startsWith("http")) return path;
  return path === "/" ? `${siteConfig.siteUrl}/` : `${siteConfig.siteUrl}${path}`;
}

export function getRouteSeo(path) {
  return siteRouteMap[path];
}

export function createSeoMetadata({ title, description, path, type = "website", image = siteConfig.defaultImage, publishedTime, modifiedTime }) {
  const url = absoluteUrl(path);
  const imageUrl = absoluteUrl(image.url);

  return {
    title,
    description,
    keywords: [
      "9jobs",
      "9 Jobs",
      "9Jobs",
      "9jobs.co",
      "9 jobs australia",
      "Resume Writing Australia",
      "LinkedIn Optimization",
      "Job Application Services",
      "ATS Resume",
      "Australia Jobs",
    ],
    alternates: {
      canonical: url,
    },
    openGraph: {
      title,
      description,
      url,
      siteName: siteConfig.name,
      images: [
        {
          ...image,
          url: imageUrl,
        },
      ],
      locale: siteConfig.locale,
      type,
      ...(publishedTime ? { publishedTime } : {}),
      ...(modifiedTime ? { modifiedTime } : {}),
    },
    twitter: {
      card: "summary_large_image",
      title,
      description,
      images: [imageUrl],
    },
  };
}

export function jsonLd(schema) {
  return JSON.stringify(schema).replace(/</g, "\\u003c");
}

export function JsonLd({ schema }) {
  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: jsonLd(schema) }}
    />
  );
}

export function createBreadcrumbSchema(items) {
  return {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    "itemListElement": items.map((item, index) => ({
      "@type": "ListItem",
      "position": index + 1,
      "name": item.name,
      "item": absoluteUrl(item.path),
    })),
  };
}

export function createFaqSchema(faqs) {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    "mainEntity": faqs.map(([question, answer]) => ({
      "@type": "Question",
      "name": question,
      "acceptedAnswer": {
        "@type": "Answer",
        "text": answer,
      },
    })),
  };
}

